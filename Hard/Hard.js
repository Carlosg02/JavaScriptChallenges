function add(p1,p2){
    return p1 + p2;
}
function subtract(p1,p2){
    return p1 - p2;
}
function multiply(p1,p2){
    return p1 * p2;
}
function divide(p1,p2){
    return p1 / p2;
}
console.log(add(2,4));
console.log(subtract(2,4));
console.log(multiply(2,4));
console.log(divide(8,2));